const int64 mod = 1e9 + 7;

template <class T> void MOD(T &x) {x = (x % mod + mod) % mod;}
template <class T> T lg(T x) {return (T)log10(x);}
template <class T> T log(T x) {return (T)log2(x);}
template <class T> T abs(T x) {return x < 0 ? -x : x;}
template <class T> T mysqrt(T x) {return std::floor(sqrtl(x));}

class WORK {
public:
	int N;

	WORK() {}

	void solve() {
		int n;
		std::cin >> n;
		int64 ans = 0;
		std::vector<int64> a;
		for (int i = 1; i <= n; i++) {
			int64 t, x;
			std::cin >> t >> x;
			while (t--) {
				MOD(ans -= a.back() * a.size());
				a.pop_back();
			}
			a.push_back(x);
			MOD(ans += x * a.size());
			std::cout << ans << endl;
		}
	}
};

int main() {
	Fast_IOS;
	WORK work;
	int T = 1;
	// std::cin >> T;
	while (T--) {
		work.solve();
	}
	return 0;
}